package br.unb.unbiquitous.ubiquitos.uos.adaptabitilyEngine;

public class NotifyException  extends Exception {

	private static final long serialVersionUID = 1717939615887225653L;
	
	public NotifyException() {
		super();
	}

	public NotifyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotifyException(String arg0) {
		super(arg0);
	}

	public NotifyException(Throwable arg0) {
		super(arg0);
	}

}
